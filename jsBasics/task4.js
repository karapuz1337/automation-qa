// 4.1
const circleRadius = 5.5
const circleArea = Math.PI * Math.pow(circleRadius, 2)
console.log(`The area of a circle is ${circleArea.toFixed(2)}`)

// 4.2
const rectLength = 6.2
const rectWidth = 4
const rectArea = rectLength * rectWidth
console.log(`The area of a rectangle is ${rectArea.toFixed(2)}`)

// 4.3
const cylinderRadius = 12.31
const cylinderHeight = 26.2
const cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * cylinderHeight
console.log(`The volume of a cylinder is ${cylinderVolume.toFixed(2)}`)

